package homeWork;

public class Variables {
    // ---- Примитивные типы ----

    byte b = 127;              // 8 бит, диапазон: -128..127
    short s = 32_767;          // 16 бит, диапазон: -32768..32767
    int i = 2_147_483_647;     // 32 бита, диапазон: -2_147_483_648..2_147_483_647
    long l = 9_223_372_036_854_775_807L; // 64 бита, суффикс L

    float f = 3.14f;           // 32 бита (IEEE 754), ~6–7 значимых цифр, суффикс f
    double d = 2.7182818284;   // 64 бита (IEEE 754), ~15 значимых цифр

    char c = 'Ж';              // 16-битный символ (UTF-16 code unit), примеры: 'A', '9', '\n', '\u263A'
    boolean bool = true;       // логический тип: true / false

    // ---- Ссылочный тип (пример) ----
    String text = "Привет, Java!"; // String — класс, переменная хранит ссылку на объект в куче
}
